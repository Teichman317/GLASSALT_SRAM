/* ================================================================== */
/*  Dial geometry constants                                           */
/* ================================================================== */
/*
 * The ASI dial is a circular instrument face with the following
 * concentric zones (from center outward):
 *
 *   HUB_R (18px)         -- center pivot hub with radial shading
 *   PTR_LEN (170px)      -- IAS and VMO pointers extend this far
 *   NUM_R (155px)         -- radial position of speed number labels
 *   TICK_INNER (171px)    -- inner edge of major tick marks
 *   TICK_MINOR_INNER      -- inner edge of minor tick marks
 *                            (DIAL_R - TICK_MIN = 183px)
 *   DIAL_R (195px)        -- outer edge of tick marks / dial ring
 *   DIAL_R+5 (200px)      -- inner edge of bezel ring
 *   DIAL_R+20 (215px)     -- outer edge of bezel ring
 *
 * Tick mark design:
 *   Major ticks are 24px long and 2px wide (TICK_MAJ = TICK_MIN*2).
 *   Minor ticks are 12px long and 1px wide.
 *   Both types share the same INNER radius (TICK_INNER) and extend
 *   outward; major ticks reach the full TICK_MAJ length while minor
 *   ticks stop at TICK_MIN. This means minor ticks appear as shorter
 *   marks starting from the same inner radius, which matches the
 *   look of a real mechanical instrument where all marks align at
 *   the inner edge.
 */
#define DIAL_R      195         /* outer edge of dial */
#define TICK_MIN    12          /* minor tick length */
#define TICK_MAJ    (TICK_MIN*2) /* major = 2x minor length */
/* Both share same inner radius; major extends outward beyond minor */
#define TICK_INNER  (DIAL_R - TICK_MAJ)  /* inner radius for major ticks */
#define TICK_MINOR_INNER (DIAL_R - TICK_MIN)  /* inner radius for minor ticks */
#define NUM_R       155         /* number center radius */
#define PTR_LEN     170         /* pointer length from center */
#define HUB_R       18          /* center hub radius */

/* ================================================================== */
/*  5x7 bitmap font                                                   */
/* ================================================================== */
/*
 * This is a minimal built-in bitmap font used for all text rendering
 * on the instrument face: speed numbers around the dial, the "IAS"
 * and "KT" labels, the Mach drum digits, the "MACH" label, and the
 * OFF/VMO flag text.
 *
 * Each glyph is 5 pixels wide and 7 pixels tall. The font data is
 * stored as an array of 7 bytes per character, where each byte
 * represents one row of the glyph. Within each byte, the most
 * significant bit used is bit 4 (0x10) for the leftmost pixel, and
 * bit 0 (0x01) for the rightmost pixel. A set bit means "draw this
 * pixel"; a clear bit means "transparent."
 *
 * Example: The digit '0' is stored as {0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E}
 *
 *   Row 0: 0x0E = 01110  →  .XXX.
 *   Row 1: 0x11 = 10001  →  X...X
 *   Row 2: 0x13 = 10011  →  X..XX  (the slash through the zero)
 *   Row 3: 0x15 = 10101  →  X.X.X
 *   Row 4: 0x19 = 11001  →  XX..X
 *   Row 5: 0x11 = 10001  →  X...X
 *   Row 6: 0x0E = 01110  →  .XXX.
 *
 * Only the characters actually needed by the ASI are included:
 * digits 0-9, letters used in labels (A,C,E,F,H,I,K,L,M,O,S,T,V),
 * and punctuation (period, space, plus, minus).
 *
 * FONT_W and FONT_H define the glyph cell size in pixels. These are
 * used by the rendering functions to compute character spacing and
 * rotation centers.
 */
#define FONT_W 5
#define FONT_H 7

